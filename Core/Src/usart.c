/**
  * @brief		重定向printf输出流
  * @param		
  */
int fputc(int ch, FILE *f)
{
    HAL_UART_Transmit(&huart1, (uint8_t *)&ch, 1, 0xFFFF);
    return ch;
}

/**
  * @brief		串口发送字符串
  * @param		_USARTx
  * @param		_pstr
  * @retval		none
  */
void USARTx_SendString(USART_TypeDef *_USARTx, char *_pstr)
{
    while(*_pstr)
    {
        _USARTx->DR = *_pstr++;
//		HAL_UART_Transmit(&huart3, *(uint8_t *)_pstr++, strlen(_pstr), 0xFFFF);
        while(!(_USARTx->SR & 1<<7)); // 利用TXE标志位判断数据包是否发送完毕
    }
}

